package Clase2;

public class Ejercicio26 {
    //Calcular la suma de los 10 primeros números naturales.
    public static void main(String args[]){
        int suma = 0;
        for (int i=1; i<=10; i++){
            suma = suma + i;
          }
        System.out.println("La suma de los 10 primeros números naturales es: " + suma);
    }

}
